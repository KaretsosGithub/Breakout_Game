int RandomUtils::getRandomInt(int max_val) {
    /*
      Gnerate a random integer
      between 0 and max_val,based
      on the computer clock.
    */
    QTime time = QTime::currentTime();
    qsrand((uint)time.msec());
    int r = qrand() % (max_val + 1);

    return r;
}

